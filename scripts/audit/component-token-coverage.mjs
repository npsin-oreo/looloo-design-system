#!/usr/bin/env node
/**
 * component-token-coverage.mjs — reports which components/ui/* have a
 * component token file under tokens/component/ (LOOLOO_DS_V2_REPO_STRUCTURE.md
 * §14.2). Writes docs/audit/component-token-coverage.md and prints a summary.
 *
 * "covered" means a draft token file EXISTS — actual runtime wiring of those
 * tokens into cva variants is the component-tokenization phase.
 *
 * Run: npm run audit:components
 */
import { readFileSync, readdirSync, writeFileSync, existsSync } from "node:fs"
import { fileURLToPath } from "node:url"
import { dirname, join } from "node:path"

const root = join(dirname(fileURLToPath(import.meta.url)), "..", "..")

// component file → token file when names differ
const TOKEN_NAME = {
  "radio-group": "radio",
  sonner: "toast",
  "navigation-menu": "navigation",
}
// components that intentionally have no token file (pure layout/utility)
const EXEMPT = new Set(["aspect-ratio", "collapsible", "direction", "form"])

// Folder-per-component layout: each subdirectory of components/ui IS a
// component (flat *.tsx at this level are compat re-export shims).
const components = readdirSync(join(root, "components", "ui"), { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name)
  .sort()

const rows = []
let covered = 0
for (const c of components) {
  const tokenFile = `${TOKEN_NAME[c] ?? c}.json`
  const has = existsSync(join(root, "tokens", "component", tokenFile))
  const status = has ? "✅ draft tokens" : EXEMPT.has(c) ? "➖ exempt" : "⬜ pending"
  if (has) covered++
  let groups = "—"
  if (has) {
    const json = JSON.parse(readFileSync(join(root, "tokens", "component", tokenFile), "utf8"))
    const rootKey = Object.keys(json).find((k) => !k.startsWith("$"))
    groups = Object.keys(json[rootKey] ?? {}).filter((k) => !k.startsWith("$")).join(", ")
  }
  rows.push(`| ${c} | ${has ? `\`tokens/component/${tokenFile}\`` : "—"} | ${groups} | ${status} |`)
}

const md = `# Component token coverage

> GENERATED by \`npm run audit:components\` (scripts/audit/component-token-coverage.mjs) — do not edit.
> "Draft tokens" = token file exists under \`tokens/component/\`; runtime wiring happens in the component-tokenization phase.

Coverage: **${covered}/${components.length}** components have draft token files (${EXEMPT.size} exempt).

| component | token file | token groups | status |
|---|---|---|---|
${rows.join("\n")}
`
writeFileSync(join(root, "docs", "audit", "component-token-coverage.md"), md)
console.log(`audit:components — ${covered}/${components.length} covered → docs/audit/component-token-coverage.md`)
